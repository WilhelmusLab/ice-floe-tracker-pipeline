    @testset "individual rotations" begin
        unit_vector(θ) = [cos(θ); sin(θ)]
        oriented_angle_between_vectors(a, b) = atan(det(hcat(a, b)), dot(a, b))
        oriented_angle_between_angles(θ1, θ2) =
            oriented_angle_between_vectors(unit_vector(θ1), unit_vector(θ2))

        function test_mask_dictionary(
            masks;
            precision_goal_degrees::Float64=10.0,
            target_fraction_ok::Float64=0.9, # TODO: Ideally this would be 1.0
        )
            results = []
            for (θ1, mask1) in masks
                for (θ2, mask2) in masks
                    Δθ = oriented_angle_between_angles(deg2rad(θ1), deg2rad(θ2))
                    Δθ_measured = IFTPipeline.get_rotation(mask1, mask2)
                    absolute_error = abs(oriented_angle_between_angles(Δθ, Δθ_measured))
                    ok = absolute_error < deg2rad(precision_goal_degrees)
                    push!(results, (; θ1, θ2, Δθ, Δθ_measured, absolute_error, ok))
                end
            end
            df = DataFrame(results)
            @info sort(df)
            fraction_ok = sum(df[:, :ok]) / length(df[:, :ok])
            @test target_fraction_ok <= fraction_ok
        end
